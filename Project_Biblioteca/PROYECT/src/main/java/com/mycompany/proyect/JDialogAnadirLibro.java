    private void anadeLibroBBDD(){
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3307/biblioteca","root","");
            System.out.println("ConectadoAnadirUsuario");
            String query = String.format("INSERT INTO libros (TITULO, AUTOR, TIPO, DISPONIBLE) VALUES(\"%s\", \"%s\", \"%s\", 1);", jTextFieldJDiaAnaLibTitulo.getText(), jTextFieldJDiaAnaLibAutor.getText(), jTextFieldJDiaAnaLibTipo.getText());
            PreparedStatement rs = con.prepareStatement(query);
            m.setRowCount(m.getRowCount());
            int rowCount = rs.executeUpdate();
            String querySelect = "SELECT (MAX(ID) + 1) AS ID FROM libros;";

            Statement stmt = con.createStatement();
            ResultSet rss = stmt.executeQuery(querySelect);
            int id = 0;
            if (rss.next())
                id = rss.getInt("ID")-1;
            else
                System.out.println("fallo en el id. poniendo el id en 1 ya que puede que se el primero");
            
            m.addRow(new Object[]{id,jTextFieldJDiaAnaLibTitulo.getText(), jTextFieldJDiaAnaLibAutor.getText(), jTextFieldJDiaAnaLibTipo.getText(), true});
            jF.preparaTablasLibDisp();
            rs.close();
            con.close();
        }catch(Exception ex){
            System.out.println("No Conectado. Error:");
            ex.printStackTrace();
        }
    }

